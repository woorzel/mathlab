export function makeT(scope) {
  return function t(key, ...args) {
    const l = getLang();
    const scopeMap = STR[scope] || {};
    const v = scopeMap[l] && scopeMap[l][key] !== undefined ? scopeMap[l][key] : (scopeMap['pl'] && scopeMap['pl'][key]);
    if (typeof v === 'function') return v(...args);
    return v;
  };
}

export default { getLang, STR, makeT };
